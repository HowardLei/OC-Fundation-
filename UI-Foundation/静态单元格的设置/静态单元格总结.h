/*
 静态单元格的创建
 单元格的创建分为动态和静态之分
    动态单元格在设计的时候只是将单元格的“框架”设计好，设置好位置，大小等基本属性，里面具体内容是根据加载的数据动态的进行修改。
    静态单元格则不会随着数据的改变而改变。当 storyboard 中设计的是什么样子，最后运行的就是什么样子。不会随着数据的变化而变化。如果想改变，必须重写修改代码。
 什么时候用动态单元格，什么时候用静态？
    当需要显示的场景内容是固定的，并且基本上不会发生改变，则这个时候需要使用静态单元格。
    当需要显示的内容会随着数据的变化而变化，则需要使用动态单元格。
 */
